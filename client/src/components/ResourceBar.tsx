import type { GameState } from "@ai-hype/shared";
import { prestigeTokenThreshold, reputationMultiplier, HARDWARE } from "@ai-hype/shared";
import { fmt, fmtRate } from "../lib/format.js";

interface Props {
  state: GameState;
}

interface ResourceProps {
  label: string;
  value: string;
  rate: string;
  rateNegative?: boolean;
  tooltip: string;
  warn?: string;
  full?: boolean;
}

function Resource({ label, value, rate, rateNegative, tooltip, warn, full }: ResourceProps) {
  return (
    <div className="resource" title={tooltip}>
      <span className="label">{label}</span>
      <span className="value">
        {value}
        {full && <span className="cap-full"> FULL</span>}
      </span>
      <span className={`rate ${rateNegative ? "negative" : ""}`}>{rate}</span>
      {warn && <span className="resource-warn">⚠ {warn}</span>}
    </div>
  );
}

/** Estimate total funding running cost from owned hardware (client-side). */
function totalFundingRunningCost(state: GameState): number {
  let total = 0;
  for (const hw of HARDWARE) {
    total += hw.fundingRunningCost * state.hardware[hw.id];
  }
  return total;
}

export function ResourceBar({ state }: Props) {
  const canPrestige = state.totalTokensEarned.gte(prestigeTokenThreshold(state.prestigeCount));
  const computeDeficit = state.computePerSecond.lt(0);

  const tokenCap = state.tokenCap;
  const computeCap = state.computeCap;
  const tokensAtCap = state.tokens.gte(tokenCap);
  const computeAtCap = state.compute.gte(computeCap);

  const fundingRunningCost = totalFundingRunningCost(state);
  const fundingDeficit = state.fundingPerSecond.lt(fundingRunningCost) && fundingRunningCost > 0;

  return (
    <div className="resource-bar">
      <Resource
        label="tokens"
        value={`${fmt(state.tokens)} / ${fmt(tokenCap)}`}
        rate={fmtRate(state.tokensPerSecond)}
        tooltip="Primary currency. Click to earn. Spent on everything."
        full={tokensAtCap}
      />
      <Resource
        label="compute"
        value={`${fmt(state.compute)} / ${fmt(computeCap)}`}
        rate={(state.computePerSecond.gte(0) ? "+" : "") + fmtRate(state.computePerSecond)}
        rateNegative={computeDeficit}
        tooltip="Generated by hardware. Consumed by models. Keep it positive."
        warn={computeDeficit ? "deficit — models offline" : undefined}
        full={computeAtCap}
      />
      <Resource
        label="hype"
        value={state.hype.toFixed(1)}
        rate={`×${(1 + state.hype * 0.05).toFixed(2)} multiplier`}
        tooltip="Multiplies token generation by (1 + hype × 0.05). Earned from milestones."
      />
      <Resource
        label="funding"
        value={`$${fmt(state.funding)}`}
        rate={fmtRate(state.fundingPerSecond)}
        tooltip="Generated by investors. Sustains high-end hardware (A100+)."
        warn={fundingDeficit ? `hardware deficit — need $${fundingRunningCost.toFixed(1)}/s` : undefined}
      />
      {state.prestigeCount > 0 && (
        <Resource
          label="reputation"
          value={String(state.reputation)}
          rate={`×${reputationMultiplier(state.reputation).toFixed(1)} bonus`}
          tooltip="Earned on prestige. Permanently multiplies all token generation. Survives resets."
        />
      )}
      {canPrestige && (
        <div className="prestige-hint">★ ready to go IPO</div>
      )}
    </div>
  );
}
